function params = respiratory_mechanics_params()
%RESPIRATORY_MECHANICS_PARAMS Shared parameters for respiratory mechanics.

params.model.compliance_L_per_cmH2O = struct( ...
    "laryngeal", 0.00127, ...
    "thoracic", 0.00238, ...
    "bronchial", 0.0131, ...
    "alveolar", 0.2, ...
    "chest_wall", 0.2445);

params.model.unstressed_volume_L = struct( ...
    "laryngeal", 34.4 / 1000, ...
    "thoracic", 6.63 / 1000, ...
    "bronchial", 18.7 / 1000, ...
    "alveolar", 1263 / 1000);

params.model.resistance_cmH2O_s_per_L = struct( ...
    "mouth_to_laryngeal", 1.021, ...
    "laryngeal_to_thoracic", 0.3369, ...
    "thoracic_to_bronchial", 0.3063, ...
    "bronchial_to_alveolar", 0.0817);

params.initial.pressure_state_cmH2O = struct( ...
    "p1", 0, ...
    "p2", 0, ...
    "p3", 0, ...
    "p4", (2.3 - params.model.unstressed_volume_L.alveolar) ...
        / params.model.compliance_L_per_cmH2O.alveolar, ...
    "p5", -5);

params.simulation.default_period_s = 5;
params.simulation.default_startup_ignore_s = 10;
params.simulation.default_duration_s = 20;

params.plot.font_size = 16;
params.plot.line_width = 1.5;
params.plot.colors = struct( ...
    "muscle_pressure", [0.00, 0.45, 0.74], ...
    "mouth_pressure", [0.85, 0.33, 0.10], ...
    "pleural_pressure", [0.00, 0.45, 0.74], ...
    "alveolar_pressure", [0.47, 0.67, 0.19], ...
    "flow", [0.49, 0.18, 0.56], ...
    "reference_flow", [0.30, 0.30, 0.30], ...
    "total_volume", [0.00, 0.45, 0.74], ...
    "alveolar_volume", [0.85, 0.33, 0.10], ...
    "dead_space_volume", [0.47, 0.67, 0.19]);
end
